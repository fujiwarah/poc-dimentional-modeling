    (floor(random() * 41) * 0.50)::numeric(5,2) AS discount
FROM orders o
CROSS JOIN LATERAL (
    -- 各注文に 1-5 件の明細を生成
    SELECT generate_series(1, (floor(random() * 5) + 1)::int) AS item_seq
) items
JOIN LATERAL (
    -- ランダムな商品を選択
    SELECT product_id, unit_price
    FROM products
    ORDER BY random()
    LIMIT 1
) p ON true;
