-- ----- orders (500件): generate_series によるプログラマティック生成 -----
-- ステータス分布: delivered 50%, shipped 15%, confirmed 15%, pending 10%, cancelled 10%
INSERT INTO orders (customer_id, order_date, shipping_address, status)
SELECT
    -- customer_id: 1-100 からランダム
    (floor(random() * 100) + 1)::int AS customer_id,
    -- order_date: 2025-04-01 ~ 2026-03-31 の範囲でランダム
    ('2025-04-01'::date + (floor(random() * 365))::int) AS order_date,
    -- shipping_address: customer_id ベースで簡略化（都道府県+市区町村）
    CASE (floor(random() * 10))::int
        WHEN 0 THEN '北海道札幌市'
        WHEN 1 THEN '東京都新宿区'
        WHEN 2 THEN '神奈川県横浜市'
        WHEN 3 THEN '愛知県名古屋市'
        WHEN 4 THEN '大阪府大阪市'
        WHEN 5 THEN '京都府京都市'
        WHEN 6 THEN '兵庫県神戸市'
        WHEN 7 THEN '福岡県福岡市'
        WHEN 8 THEN '宮城県仙台市'
        ELSE '広島県広島市'
    END AS shipping_address,
    -- status: delivered が最多になる分布
    CASE
        WHEN g <= 250 THEN 'delivered'
        WHEN g <= 325 THEN 'shipped'
        WHEN g <= 400 THEN 'confirmed'
        WHEN g <= 450 THEN 'pending'
        ELSE 'cancelled'
    END AS status
FROM generate_series(1, 500) AS g;

